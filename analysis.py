def statistics_sort(ciphertext):
    list = statistics(ciphertext)
    sortlist = []  # empty List
    for i in range(26):
        sortlist.append((chr(65 + i), list[i]))
    tri = sorted(sortlist, key=lambda freq: freq[1], reverse=True)
    return (tri)


