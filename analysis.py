def statistics(ciphertext):
    list_stat = [0 for x in range(26)]  # A liste with  0s
    for letter in ciphertext:  # go through text
        i = ord(letter) - 65
        if 0 <= i < 26:  # make sure it's a letter
            list_stat[i] = list_stat[i] + 1
    return (list_stat)


